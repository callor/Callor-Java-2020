package com.callor.varriable;

public class Numbers {

	public static void main(String[] args) {

		System.out.println("===========================================");
		System.out.println("정수와 실수의 나눗셈, 나머지 연산");
		System.out.println("-------------------------------------------");
		System.out.println("30 / 40 = " + 30 / 40);
		System.out.println("40 / 30 = " + 40 / 30);
		System.out.println("40.0 / 30.0 = " + 40.0 / 30.0);
		System.out.println("40.0 % 30.0 = " + 40.0 % 30.0);

		
		System.out.println("===========================================");
		System.out.println("정수의 4칙연산과 나머지 연산");
		
		System.out.println("-------------------------------------------");
		System.out.println(" 33 + 2 = " + (33 + 2 ));
		System.out.println(" 33 - 2 = " + (33 - 2 ));
		System.out.println(" 33 * 2 = " + (33 * 2 ));
		System.out.println("-------------------------------------------");
		System.out.println("정수의 나눗셈을 몫을 나타낸다");
		System.out.println("-------------------------------------------");
		System.out.println(" 33 / 2 = " + (33 / 2 ));
		System.out.println(" 33 % 2 = " + (33 % 2 ));
		
		System.out.println("===========================================");
		System.out.println("실수의 4칙연산과 나머지 연산");
		System.out.println("-------------------------------------------");
		System.out.println(" 33.0 + 2 = " + (33.0 + 2 ));
		System.out.println(" 33.0 - 2 = " + (33.0 - 2 ));
		System.out.println(" 33.0 * 2 = " + (33.0 * 2 ));
		System.out.println(" 33.0 / 2 = " + (33.0 / 2 ));
		System.out.println(" 33.0 % 2 = " + (33.0 % 2 ));
		
		
		
	}

	@Override
	public String toString() {
		// TODO Auto-generated method stub
		return super.toString();
	}

	
}
